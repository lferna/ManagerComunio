package grupo;

import usuario.UsuarioVO;

public class GrupoVO {

	private String grupo;
	private String hash;
	private UsuarioVO[] usuarios;
	
	public GrupoVO(){
		
	}
	
	public GrupoVO(String grupo, String hash){
		this.grupo = grupo;
		this.hash = hash;
	}

	public String getGrupo() {
		return grupo;
	}

	public void setGrupo(String grupo) {
		this.grupo = grupo;
	}

	public String getHash() {
		return hash;
	}

	public void setHash(String hash) {
		this.hash = hash;
	}

	public UsuarioVO[] getUsuarios() {
		return usuarios;
	}

	public void setUsuarios(UsuarioVO[] usuarios) {
		this.usuarios = usuarios;
	}
	
	
}
