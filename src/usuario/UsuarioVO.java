package usuario;

public class UsuarioVO {

	
	private String idUsuario;
	private String hash;
	private String grupo;
	private String email;
	private String idTactica;
	public UsuarioVO(){
		this.idUsuario = "";
		this.hash = "";
		this.grupo = "";
		this.email = "";
		this.idTactica = "";
	}
	
	public UsuarioVO(String idUsuario, String hash, String grupo, String email, String idTactica){
		this.idUsuario = idUsuario;
		this.hash = hash;		
		this.grupo = grupo;
		this.email = email;
		this.idTactica = idTactica;
	}

	public String getIdUsuario() {
		return idUsuario;
	}

	public void setIdUsuario(String idUsuario) {
		this.idUsuario = idUsuario;
	}

	public String getHash() {
		return hash;
	}

	public void setHash(String hash) {
		this.hash = hash;
	}

	public String getGrupo() {
		return grupo;
	}

	public void setGrupo(String grupo) {
		this.grupo = grupo;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getIdTactica() {
		return idTactica;
	}

	public void setIdTactica(String idTactica) {
		this.idTactica = idTactica;
	}
	
	

}
